package com.enrique.leon.ClassModel;

public class QueryPair {
	private String key;
	private String value;
	public QueryPair(String key, String value) {
		super();
		this.key = key;
		this.value = value;
	}
	public String getKey() {
		return key;
	}
	public String getValue() {
		return value;
	}
}
